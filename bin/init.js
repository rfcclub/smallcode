#!/usr/bin/env node
// SmallCode — Config Wizard (smallcode init)
// Interactive setup that creates a .env file for the user.

const readline = require('readline');
const fs = require('fs');
const path = require('path');

const PROVIDERS = {
  'lmstudio': { name: 'LM Studio', defaultUrl: 'http://localhost:1234/v1' },
  'ollama': { name: 'Ollama', defaultUrl: 'http://localhost:11434/v1' },
  'openrouter': { name: 'OpenRouter', defaultUrl: 'https://openrouter.ai/api/v1' },
  'openai': { name: 'OpenAI', defaultUrl: 'https://api.openai.com/v1' },
  'custom': { name: 'Custom endpoint', defaultUrl: '' },
};

async function ask(rl, question, defaultVal) {
  return new Promise((resolve) => {
    const prompt = defaultVal ? `${question} [${defaultVal}]: ` : `${question}: `;
    rl.question(prompt, (answer) => {
      resolve(answer.trim() || defaultVal || '');
    });
  });
}

async function main() {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });

  console.log(`
  ⚡ SmallCode Setup
  ─────────────────────────────────
  `);

  // Provider
  console.log('  Providers:');
  console.log('    1) LM Studio (local, default port 1234)');
  console.log('    2) Ollama (local, default port 11434)');
  console.log('    3) OpenRouter (cloud, needs API key)');
  console.log('    4) OpenAI (cloud, needs API key)');
  console.log('    5) Custom endpoint');
  console.log('');

  const providerChoice = await ask(rl, '  Choose provider (1-5)', '1');
  const providerMap = { '1': 'lmstudio', '2': 'ollama', '3': 'openrouter', '4': 'openai', '5': 'custom' };
  const providerId = providerMap[providerChoice] || 'lmstudio';
  const provider = PROVIDERS[providerId];

  // Base URL
  let baseUrl = provider.defaultUrl;
  if (providerId === 'custom' || !baseUrl) {
    baseUrl = await ask(rl, '  Base URL (e.g. http://host:port/v1)', 'http://localhost:1234/v1');
  } else {
    const customUrl = await ask(rl, `  Base URL`, baseUrl);
    if (customUrl) baseUrl = customUrl;
  }

  // Model name
  let modelName = '';
  if (providerId === 'openrouter') {
    modelName = await ask(rl, '  Model (e.g. openai/gpt-4o-mini, google/gemma-2-9b-it)', 'openai/gpt-4o-mini');
  } else if (providerId === 'openai') {
    modelName = await ask(rl, '  Model', 'gpt-4o-mini');
  } else {
    modelName = await ask(rl, '  Model name (as shown in your server)', '');
    if (!modelName) {
      console.log('  ⚠ No model specified. SmallCode will try to auto-detect from the endpoint.');
    }
  }

  // API key (only for cloud providers)
  let apiKey = '';
  if (providerId === 'openrouter' || providerId === 'openai') {
    apiKey = await ask(rl, '  API Key', '');
    if (!apiKey) {
      console.log('  ⚠ No API key. Set OPENAI_API_KEY in .env later.');
    }
  } else {
    const needsKey = await ask(rl, '  Does your server require an API key? (y/N)', 'n');
    if (needsKey.toLowerCase() === 'y') {
      apiKey = await ask(rl, '  API Key', '');
    }
  }

  rl.close();

  // Build .env content
  const lines = [
    '# SmallCode configuration (generated by smallcode init)',
    `SMALLCODE_MODEL=${modelName}`,
    `SMALLCODE_BASE_URL=${baseUrl}`,
    `SMALLCODE_PROVIDER=openai`,
  ];
  if (apiKey) {
    lines.push(`OPENAI_API_KEY=${apiKey}`);
  }
  lines.push('');

  const envPath = path.join(process.cwd(), '.env');
  const exists = fs.existsSync(envPath);

  if (exists) {
    console.log(`\n  ⚠ .env already exists. Overwrite? (Ctrl+C to cancel)`);
    // Just write — they can Ctrl+C if they don't want it
  }

  fs.writeFileSync(envPath, lines.join('\n'));
  console.log(`\n  ✓ Created ${envPath}`);
  console.log(`\n  Run \`smallcode\` to start!\n`);
}

main().catch((e) => {
  if (e.message !== 'readline was closed') {
    console.error(e);
  }
  process.exit(0);
});
